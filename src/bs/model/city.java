package bs.model;

public class city {
	private int ciid;
	private int sid;
	private String ciiname;
	private String ciimap;
	private String ciidesc;
	public int getCiid() {
		return ciid;
	}
	public void setCiid(int ciid) {
		this.ciid = ciid;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getCiiname() {
		return ciiname;
	}
	public void setCiiname(String ciiname) {
		this.ciiname = ciiname;
	}
	public String getCiimap() {
		return ciimap;
	}
	public void setCiimap(String ciimap) {
		this.ciimap = ciimap;
	}
	public String getCiidesc() {
		return ciidesc;
	}
	public void setCiidesc(String ciidesc) {
		this.ciidesc = ciidesc;
	}
}
